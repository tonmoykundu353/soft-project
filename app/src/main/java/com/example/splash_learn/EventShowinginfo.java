package com.example.splash_learn;

public class EventShowinginfo {
    String clubname,eventdate,eventname,eventplace,eventRegFees,organizedBy;

    public EventShowinginfo(String clubname, String eventdate, String eventname, String eventplace, String eventRegFees, String organizedBy) {
        this.clubname = clubname;
        this.eventdate = eventdate;
        this.eventname = eventname;
        this.eventplace = eventplace;
        this.eventRegFees = eventRegFees;
        this.organizedBy = organizedBy;
    }

    public EventShowinginfo() {

    }

    public String getClubname() {
        return clubname;
    }

    public void setClubname(String clubname) {
        this.clubname = clubname;
    }

    public String getEventdate() {
        return eventdate;
    }

    public void setEventdate(String eventdate) {
        this.eventdate = eventdate;
    }

    public String getEventname() {
        return eventname;
    }

    public void setEventname(String eventname) {
        this.eventname = eventname;
    }

    public String getEventplace() {
        return eventplace;
    }

    public void setEventplace(String eventplace) {
        this.eventplace = eventplace;
    }

    public String getEventRegFees() {
        return eventRegFees;
    }

    public void setEventRegFees(String eventRegFees) {
        this.eventRegFees = eventRegFees;
    }

    public String getOrganizedBy() {
        return organizedBy;
    }

    public void setOrganizedBy(String organizedBy) {
        this.organizedBy = organizedBy;
    }
}
